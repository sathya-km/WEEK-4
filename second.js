// Using a for loop
console.log("Using a for loop:");
for (let i = 1; i <= 10; i++) {
    console.log(i);
}

// Using a while loop
console.log("Using a while loop:");
let j = 1;
while (j <= 10) {
    console.log(j);
    j++;
}

// Using a do...while loop
console.log("Using a do...while loop:");
let k = 1;
do {
    console.log(k);
    k++;
} while (k <= 10);
